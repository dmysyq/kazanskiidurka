#!/usr/bin/env python3

# Вариант 4. Мини-словарь соответствий
# Программа для студентов-филологов
#
# Что делает эта программа:
# 1. Сначала считывает целое число N — сколько пар слов будет введено.
# 2. Затем N раз считывает строки вида: "слово1 слово2".
#    Предполагается, что между словами один или несколько пробелов.
# 3. Создаёт словарь соответствий: слово1 -> слово2.
# 4. Затем считывает одно слово-запрос.
# 5. Если слово-запрос есть среди ключей словаря, выводит слово2.
#    Если такого слова нет, выводит "Нет в словаре".
#
# Здесь полезно понять:
# - split() разобьёт строку "слово1 слово2" на список ["слово1", "слово2"].
# - словарь dict в Python хранит пары "ключ: значение".


def main():
    # 1. Считываем количество пар слов N.
    #    input() возвращает строку, поэтому сначала чтение строки,
    #    затем преобразование к целому числу с помощью int().
    n_str = input()
    n = int(n_str)

    # 2. Создаём пустой словарь для хранения соответствий.
    dictionary = {}

    # 3. Считываем N строк с парами слов.
    for i in range(n):
        line = input()
        # Разбиваем строку на части: ожидаем не менее двух слов.
        parts = line.split()
        # Если частей не меньше двух, берём первые два.
        if len(parts) >= 2:
            word1 = parts[0]
            word2 = parts[1]
            # Добавляем в словарь соответствие: word1 -> word2
            dictionary[word1] = word2
        else:
            # Если вдруг строка не содержит двух слов, просто пропускаем её.
            continue

    # 4. Считываем слово-запрос.
    query = input()

    # 5. Проверяем, есть ли слово в словаре.
    if query in dictionary:
        # Если есть, выводим связанное с ним слово.
        print(dictionary[query])
    else:
        # Если нет, выводим строго заданную фразу.
        print("Нет в словаре")


if __name__ == "__main__":
    main()

