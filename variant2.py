#!/usr/bin/env python3

# Вариант 2. Статистика текста
# Программа для студентов-филологов
#
# Что делает эта программа:
# 1. Считывает несколько строк текста. Ввод заканчивается
#    ПУСТОЙ строкой (пользователь просто нажимает Enter).
# 2. Переводит весь текст в нижний регистр.
# 3. Разбивает текст на слова.
# 4. Считает:
#    - общее количество слов (X),
#    - количество различных слов (Y).
# 5. Выводит:
#    "Всего слов: X"
#    "Различных слов: Y"
#
# Пояснение: здесь мы НЕ убираем знаки препинания специально,
# так как в задании это не требуется. Слова разделяются пробелом
# и другими пробельными символами.


def main():
    # 1. Считываем все строки до пустой строки.
    lines = []  # здесь будем хранить все введённые строки

    while True:
        line = input()
        # Пустая строка — сигнал окончания ввода
        if line == "":
            break
        # Добавляем строку в список
        lines.append(line)

    # 2. Объединяем все строки в один большой текст.
    #    Между строками ставим пробел, чтобы слова не "слиплись".
    #    " ".join(список) соединяет элементы списка строк через пробел.
    full_text = " ".join(lines)

    # Переводим текст в нижний регистр
    full_text = full_text.lower()

    # 3. Разбиваем текст на слова.
    words = full_text.split()

    # 4. Считаем общее количество слов.
    total_words = len(words)

    # 5. Считаем количество различных слов.
    #    Для этого удобно использовать множество (set):
    #    оно хранит только уникальные элементы.
    unique_words = set(words)
    different_words = len(unique_words)

    # 6. Выводим результат в строгом формате.
    print("Всего слов:", total_words)
    print("Различных слов:", different_words)


if __name__ == "__main__":
    main()

