#!/usr/bin/env python3

# Вариант 1. Частотный анализ текста
# Программа для студентов-филологов
#
# Что делает эта программа:
# 1. Считывает одну строку текста (через input()).
# 2. Переводит текст в нижний регистр (чтобы "Мама" и "мама" считались одним словом).
# 3. Удаляет знаки препинания: . , : ; ! ?
# 4. Разбивает строку на слова (по пробелам и другим пробельным символам).
# 5. Считает, сколько раз каждое слово встретилось.
# 6. Выводит все слова и их количества, в алфавитном порядке.
#
# Используются только базовые средства Python: строки, списки, словари, циклы, if.


def main():
    # 1. Считываем одну строку текста
    text = input()

    # 2. Переводим весь текст в нижний регистр
    #    lower() возвращает новую строку, в которой все буквы маленькие
    text = text.lower()

    # 3. Удаляем знаки препинания.
    #    Для простоты: по очереди заменяем каждый знак препинания на пустую строку.
    punctuation = ".,:;!?"
    for symbol in punctuation:
        # text.replace(old, new) возвращает новую строку, где все old заменены на new
        text = text.replace(symbol, "")

    # 4. Разбиваем текст на слова.
    #    split() без аргументов делит по любым пробельным символам
    words = text.split()

    # 5. Считаем, сколько раз каждое слово встречается.
    #    Для этого используем словарь: ключ — слово, значение — количество.
    word_counts = {}

    for word in words:
        # Если слово уже есть в словаре, увеличиваем счётчик,
        # иначе добавляем со значением 1.
        if word in word_counts:
            word_counts[word] = word_counts[word] + 1
        else:
            word_counts[word] = 1

    # 6. Получаем список всех слов и сортируем его по алфавиту.
    #    sorted(...) возвращает новый отсортированный список ключей словаря.
    sorted_words = sorted(word_counts.keys())

    # 7. Выводим каждое слово и его количество в нужном формате:
    #    слово количество
    for word in sorted_words:
        count = word_counts[word]
        print(word, count)


if __name__ == "__main__":
    main()

