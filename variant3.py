#!/usr/bin/env python3

# Вариант 3. Лексическое разнообразие текста
# Программа для студентов-филологов
#
# Что делает эта программа:
# 1. Считывает одну строку текста.
# 2. Разбивает текст на слова (по пробелам и другим пробелам).
# 3. Считает:
#    - общее количество слов,
#    - количество различных (уникальных) слов.
# 4. Вычисляет коэффициент лексического разнообразия:
#    K = (количество различных слов) / (общее количество слов)
# 5. Выводит:
#    "Слов всего: ..."
#    "Различных слов: ..."
#    "Коэффициент: ..."
#    Коэффициент выводим с точностью до трёх знаков после запятой.
#
# Замечание: в условии не требуется явно приводить к нижнему регистру
# или убирать знаки препинания, поэтому здесь мы этого не делаем.


def main():
    # 1. Считываем одну строку текста
    text = input()

    # 2. Разбиваем текст на слова
    words = text.split()

    # 3. Общее количество слов
    total_words = len(words)

    # Обрабатываем особый случай: если слов вообще нет (пустая строка),
    # чтобы не делить на ноль. В таком случае считаем, что коэффициент 0.0.
    if total_words == 0:
        different_words = 0
        diversity = 0.0
    else:
        # 4. Количество различных слов с помощью множества
        unique_words = set(words)
        different_words = len(unique_words)

        # 5. Вычисляем коэффициент
        diversity = different_words / total_words

    # 6. Вывод результата
    print("Слов всего:", total_words)
    print("Различных слов:", different_words)

    # Форматируем коэффициент с точностью до трёх знаков после запятой.
    # format(diversity, ".3f") даёт строку вида "0.123"
    diversity_str = format(diversity, ".3f")
    print("Коэффициент:", diversity_str)


if __name__ == "__main__":
    main()

